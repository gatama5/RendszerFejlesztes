
class Car:
    def __init__(self, id, licensePlate, brand, model, year, fuelType, km, state, available):
        self.id = id
        self.licensePlate = licensePlate
        self.brand = brand
        self.model = model
        self.year = year
        self.fuelType = fuelType
        self.km = km
        self.state = state
        self.available = available